    private void modificarCorrequisitsAssignaturaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarCorrequisitsAssignaturaButtonActionPerformed
        int col=jTable1.getSelectedColumn();
        int row=jTable1.getSelectedRow();
        if (col != -1){
            if (row != -1) {
                String nomAssignatura=jTable1.getValueAt(row,0).toString();
                cp.creaVistaCorrequisits(nomAssignatura,nomPlaEstudis);
            }
        }
    }//GEN-LAST:event_modificarCorrequisitsAssignaturaButtonActionPerformed

    private void modificarAssignaturaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarAssignaturaButtonActionPerformed
        int col=jTable1.getSelectedColumn();
        int row=jTable1.getSelectedRow();
        if (col != -1){
            if (row != -1) {
                String nomAssignatura=jTable1.getValueAt(row,0).toString();
                String fase=jTable1.getValueAt(row,1).toString();
                String capacitatGrup=jTable1.getValueAt(row,2).toString();
                String capacitatSubGrup=jTable1.getValueAt(row,3).toString();
                String matriculats=jTable1.getValueAt(row,4).toString();
                String tipusSubGrup=jTable1.getValueAt(row,5).toString();
                String numSessions=jTable1.getValueAt(row,6).toString();
                String duracio=jTable1.getValueAt(row,7).toString();
                info[0]=nomAssignatura;
                info[1]=fase;
                info[2]=capacitatGrup;
                info[3]=capacitatSubGrup;
                info[4]=matriculats;
                info[5]=tipusSubGrup;
                info[6]=numSessions;
                info[7]=duracio;
                DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
                tm.removeRow(jTable1.getSelectedRow());
                
                infoAssignaturesDialog i = new infoAssignaturesDialog(new javax.swing.JFrame(),true,cp,info,true);
                i.setVisible(true);
                                
            }
        }                 
    }//GEN-LAST:event_modificarAssignaturaButtonActionPerformed


