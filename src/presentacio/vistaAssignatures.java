    void afegirAssignatura(String nomAssignatura, String fase, String capacitatGrup, String capacitatSubgrup, String matriculats, String tipusSubgrup, String numSessions, String duracio) {
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        tm.addRow(new Object[]{nomAssignatura, fase, capacitatGrup, capacitatSubgrup, matriculats, tipusSubgrup, numSessions, duracio});
    }

