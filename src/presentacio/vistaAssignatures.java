    private void eliminarAssignaturaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarAssignaturaButtonActionPerformed
        int col=jTable1.getSelectedColumn();
        int row=jTable1.getSelectedRow();
        if (col != -1){
            if (row != -1) {
                errorDialog ce= new errorDialog(new javax.swing.JFrame(),true,jTable1.getValueAt(row, 0).toString(),"",false);
                ce.setVisible(true);
                if(ce.getReturnStatus()==1){
                    String nomAssignatura=jTable1.getValueAt(row,0).toString();
                    cp.esborrarAssignatura(nomAssignatura,nomPlaEstudis);
                    DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
                    tm.removeRow(jTable1.getSelectedRow());
                }
            }
        }
    }//GEN-LAST:event_eliminarAssignaturaButtonActionPerformed

