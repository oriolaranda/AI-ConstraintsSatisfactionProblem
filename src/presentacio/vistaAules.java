    private void modificarAulaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarAulaButtonActionPerformed
        int col=jTable1.getSelectedColumn();
        int row=jTable1.getSelectedRow();
        if (col != -1){
            if (row != -1) {
                String nomAula=jTable1.getValueAt(row,0).toString();
                String capacitat=jTable1.getValueAt(row,1).toString();
                String tipus=jTable1.getValueAt(row,2).toString();
                info[0]=nomAula;
                info[1]=capacitat;
                info[2]=tipus;
                
                DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
                tm.removeRow(jTable1.getSelectedRow());
                
                infoAulesDialog i = new infoAulesDialog(new javax.swing.JFrame(),true,cp,info,true);
                i.setVisible(true);
                                
            }
        }
    }//GEN-LAST:event_modificarAulaButtonActionPerformed
    public void afegirAula(String nom,String capacitat,String tipus){

        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        tm.addRow(new Object[]{nom, capacitat, tipus});
        
    }

